package com.agree.abt.model.dataAnalysis;

import java.io.Serializable;

/**
 * 凭证信息表
 * @ClassName: BtBusVoucherInfo.java
 * @company 赞同科技
 * @author Hujuqiang
 * @date 2016-10-28
 */
public class BtBusVoucherInfo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String vouchercode;
	private String vouchertype;
	private String manufacturertype;
	private String voucherpicture;
	private String vouchercodename;
	private String voucherstatus;
	private String picturemd5;
	private String voucherpicturepath;
	public BtBusVoucherInfo() {
		super();
	}
	public BtBusVoucherInfo(String vouchertype, String manufacturertype,
			String voucherpicture, String vouchercodename, String voucherstatus,String picturemd5, String voucherpicturepath) {
		super();
		this.vouchertype = vouchertype;
		this.manufacturertype = manufacturertype;
		this.voucherpicture = voucherpicture;
		this.vouchercodename = vouchercodename;
		this.voucherstatus = voucherstatus;
		this.picturemd5 = picturemd5;
		this.voucherpicturepath = voucherpicturepath;
	}
	public String getVouchercode() {
		return vouchercode;
	}
	public void setVouchercode(String vouchercode) {
		this.vouchercode = vouchercode;
	}
	public String getVouchertype() {
		return vouchertype;
	}
	public void setVouchertype(String vouchertype) {
		this.vouchertype = vouchertype;
	}
	public String getManufacturertype() {
		return manufacturertype;
	}
	public void setManufacturertype(String manufacturertype) {
		this.manufacturertype = manufacturertype;
	}
	public String getVoucherpicture() {
		return voucherpicture;
	}
	public void setVoucherpicture(String voucherpicture) {
		this.voucherpicture = voucherpicture;
	}
	public String getVouchercodename() {
		return vouchercodename;
	}
	public void setVouchercodename(String vouchercodename) {
		this.vouchercodename = vouchercodename;
	}
	public String getVoucherstatus() {
		return voucherstatus;
	}
	public void setVoucherstatus(String voucherstatus) {
		this.voucherstatus = voucherstatus;
	}
	public String getPicturemd5() {
		return picturemd5;
	}
	public void setPicturemd5(String picturemd5) {
		this.picturemd5 = picturemd5;
	}
	public String getVoucherpicturepath() {
		return voucherpicturepath;
	}
	public void setVoucherpicturepath(String voucherpicturepath) {
		this.voucherpicturepath = voucherpicturepath;
	}
	
}
