package com.agree.abt.model.confManager;
/**
 * 主题对象类
 * @ClassName: ThemeInfo.java
 * @company 赞同科技
 * @author XiWang
 * @date 2014-4-16
 */
public class ThemeInfo {
	private String theme_id;
	private String theme_name;
	private String theme_imgsrc_icon;
	private String theme_imgsrc_list;
	private String theme_note;
	private String branch;
	private String default_flag;
	
	public ThemeInfo() {
		super();
	}
	
	public ThemeInfo(String theme_id, String theme_name,
			String theme_imgsrc_icon, String theme_imgsrc_list,
			String theme_note, String branch, String default_flag) {
		super();
		this.theme_id = theme_id;
		this.theme_name = theme_name;
		this.theme_imgsrc_icon = theme_imgsrc_icon;
		this.theme_imgsrc_list = theme_imgsrc_list;
		this.theme_note = theme_note;
		this.branch = branch;
		this.default_flag = default_flag;
	}
	
	public String getTheme_id() {
		return theme_id;
	}
	public void setTheme_id(String theme_id) {
		this.theme_id = theme_id;
	}
	public String getTheme_name() {
		return theme_name;
	}
	public void setTheme_name(String theme_name) {
		this.theme_name = theme_name;
	}
	public String getTheme_imgsrc_icon() {
		return theme_imgsrc_icon;
	}
	public void setTheme_imgsrc_icon(String theme_imgsrc_icon) {
		this.theme_imgsrc_icon = theme_imgsrc_icon;
	}
	public String getTheme_imgsrc_list() {
		return theme_imgsrc_list;
	}
	public void setTheme_imgsrc_list(String theme_imgsrc_list) {
		this.theme_imgsrc_list = theme_imgsrc_list;
	}
	public String getTheme_note() {
		return theme_note;
	}
	public void setTheme_note(String theme_note) {
		this.theme_note = theme_note;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getDefault_flag() {
		return default_flag;
	}
	public void setDefault_flag(String default_flag) {
		this.default_flag = default_flag;
	}
}
