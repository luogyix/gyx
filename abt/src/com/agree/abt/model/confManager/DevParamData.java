package com.agree.abt.model.confManager;
/**
 * 设备私有参数	
 * @author linyuedong
 *
 */
public class DevParamData {
	private String laying_mode;//在离行
	private String buydate;//购置日期
	private String installdate;//安装日期
	private String card_admin;//发卡机管理员
	private String device_assessor;//审核员
	private String device_address;//装机地址
	private String parameter_id;//菜单参数
	public String getParameter_id() {
		return parameter_id;
	}
	public void setParameter_id(String parameter_id) {
		this.parameter_id = parameter_id;
	}
	public String getLaying_mode() {
		return laying_mode;
	}
	public void setLaying_mode(String laying_mode) {
		this.laying_mode = laying_mode;
	}
	public String getBuydate() {
		return buydate;
	}
	public void setBuydate(String buydate) {
		this.buydate = buydate;
	}
	public String getInstalldate() {
		return installdate;
	}
	public void setInstalldate(String installdate) {
		this.installdate = installdate;
	}
	public String getCard_admin() {
		return card_admin;
	}
	public void setCard_admin(String card_admin) {
		this.card_admin = card_admin;
	}
	public String getDevice_assessor() {
		return device_assessor;
	}
	public void setDevice_assessor(String device_assessor) {
		this.device_assessor = device_assessor;
	}
	public String getDevice_address() {
		return device_address;
	}
	public void setDevice_address(String device_address) {
		this.device_address = device_address;
	}
	
	public DevParamData(String laying_mode, String buydate, String installdate,
			String card_admin, String device_assessor, String device_address,String parameter_id) {
		super();
		this.laying_mode = laying_mode;
		this.buydate = buydate;
		this.installdate = installdate;
		this.card_admin = card_admin;
		this.device_assessor = device_assessor;
		this.device_address = device_address;
		this.parameter_id = parameter_id;
	}
	public DevParamData() {
		super();
	}
	
}
