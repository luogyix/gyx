package com.agree.abt.model.confManager;

import java.io.Serializable;

import com.agree.framework.web.form.administration.Unit;

/**
 * 临近网点实体类
 * 
 * @ClassName: BtSysBranchNeig.java
 * @company 赞同科技
 * @author XiWang
 * @date 2015-1-14
 */
public class BtSysBranchNeig implements Serializable {

	private static final long serialVersionUID = 1L;

	/**
	 * 机构号
	 */
	private String branch;
	/**
	 * 临近网点机构号
	 */
	private String branch_neighbous;
	/**
	 * 备注
	 */
	private String note;
	/**
	 * 临时网点(不明)
	 */
	private String branch_temp;
	
	private Unit unit;

	public String getBranch() {
		return branch;
	}

	public void setBranch(String branch) {
		this.branch = branch;
	}

	public String getBranch_neighbous() {
		return branch_neighbous;
	}

	public void setBranch_neighbous(String branch_neighbous) {
		this.branch_neighbous = branch_neighbous;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public Unit getUnit() {
		return unit;
	}

	public void setUnit(Unit unit) {
		this.unit = unit;
	}

	public String getBranch_temp() {
		return branch_temp;
	}

	public void setBranch_temp(String branch_temp) {
		this.branch_temp = branch_temp;
	}
}
