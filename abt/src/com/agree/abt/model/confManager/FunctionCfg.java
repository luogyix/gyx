package com.agree.abt.model.confManager;

/**
 * 
 * @ClassName: FunctionCfg.java
 * @company 赞同科技
 * @author XiWang
 * @date 2014-4-25
 */
public class FunctionCfg {
	private String function_id;
	private String function_name;
	private String function_status;
	private String branch;
	private String function_flag;
	
	public FunctionCfg() {
		super();
	}
	public FunctionCfg(String function_id, String function_name,
			String function_status, String branch, String function_flag) {
		super();
		this.function_id = function_id;
		this.function_name = function_name;
		this.function_status = function_status;
		this.branch = branch;
		this.function_flag = function_flag;
	}
	public String getFunction_id() {
		return function_id;
	}
	public void setFunction_id(String function_id) {
		this.function_id = function_id;
	}
	public String getFunction_name() {
		return function_name;
	}
	public void setFunction_name(String function_name) {
		this.function_name = function_name;
	}
	public String getFunction_status() {
		return function_status;
	}
	public void setFunction_status(String function_status) {
		this.function_status = function_status;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getFunction_flag() {
		return function_flag;
	}
	public void setFunction_flag(String function_flag) {
		this.function_flag = function_flag;
	}
}
