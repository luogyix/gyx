	private static final long serialVersionUID = 1L;
	
	private String brno;//机构号
	private String type;//机构类型-0 总行1 分行2 支行3 网点
	private String supervbrno;//管辖机构号
	private String branchnames;//机构名称简称
	private String branchname;//机构名称全称
