package com.agree.abt.model.pfs;

import java.io.Serializable;

/**
 * 机构费率信息实体类
 * @ClassName: BtCustRate.java
 * @company 赞同科技
 * @author 王明哲
 * @date 2016-12-27
 */
public class BtCustRate implements Serializable
{
	private static final long serialVersionUID = 4155203680411371309L;
	/**
	 * 机构号
	 */
	private String branch;
	/**
	 * 产品名称
	 */
	private String produce_name;
	/**
	 * 收费项目
	 */
	private String charge_project;
	/**
	 * 收费标准
	 */
	private String charge_criterion;
	/**
	 * 收费依据
	 */
	private String charge_reason;
	/**
	 * 创建时间
	 */
	private String create_date;
	/**
	 * 备注
	 */
	private String cust_note;
	/**
	 * 修改时间
	 */
	private String update_time;
	
	public String getBranch() 
	{
		return branch;
	}
	public void setBranch(String branch)
	{
		this.branch = branch;
	}
	public String getProduce_name() 
	{
		return produce_name;
	}
	public void setProduce_name(String produce_name) 
	{
		this.produce_name = produce_name;
	}
	public String getCharge_project() 
	{
		return charge_project;
	}
	public void setCharge_project(String charge_project) 
	{
		this.charge_project = charge_project;
	}
	public String getCharge_criterion() 
	{
		return charge_criterion;
	}
	public void setCharge_criterion(String charge_criterion) 
	{
		this.charge_criterion = charge_criterion;
	}
	public String getCharge_reason() 
	{
		return charge_reason;
	}
	public void setCharge_reason(String charge_reason) 
	{
		this.charge_reason = charge_reason;
	}
	public String getCreate_date() 
	{
		return create_date;
	}
	public void setCreate_date(String crate_date) 
	{
		this.create_date = crate_date;
	}
	public String getCust_note() 
	{
		return cust_note;
	}
	public void setCust_note(String cust_note) 
	{
		this.cust_note = cust_note;
	}
	public String getUpdate_time() 
	{
		return update_time;
	}
	public void setUpdate_time(String update_time) 
	{
		this.update_time = update_time;
	}
}
